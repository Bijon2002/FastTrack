    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        int driverId = Integer.parseInt(comboDriver.getSelectedItem().toString());
        int shipmentId = Integer.parseInt(comboShipment.getSelectedItem().toString());
        String message="";
        
        
        String email = null;

    try {
        Connection con = DBConnection.getConnection(); // Your DB connector
        String sql = "SELECT email FROM drivers WHERE driver_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setInt(1, driverId);

        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            email = rs.getString("email");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "‚ùå Failed to get driver email.");
    }
    
    try {
        Connection con = DBConnection.getConnection();
        String sql = "SELECT sender, receiver, distance, duration FROM shipments WHERE id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setInt(1, shipmentId);

        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            String sender = rs.getString("sender");
            String receiver = rs.getString("receiver");
            double distance = rs.getDouble("distance");
            String duration = rs.getString("duration");

            message = "üì¶ Shipment Details:\n"
                    + "üöö From: " + sender + "\n"
                    + "üìç To: " + receiver + "\n"
                    + "üõ£Ô∏è Distance: " + distance + " km\n"
                    + "‚è±Ô∏è Estimated Delivery Time: " + duration;
        } else {
            System.out.println("‚ùå No shipment found with ID: " + shipmentId); 
        }

    } catch (Exception e) {
        e.printStackTrace();
        message = "‚ùå Error fetching shipment data.";
    }

    assignDriver();
    SimpleMailer.sendEmail(email, "Shipments Assingments", message);
    

    }//GEN-LAST:event_btnAssignActionPerformed

