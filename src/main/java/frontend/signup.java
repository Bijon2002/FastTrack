    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        String name = fullname.getText().trim();
    String emailText = email.getText().trim();
    String pass = password.getText().trim();
    String addr = address.getText().trim();
    String phn = phone.getText().trim();

    // üîç VALIDATIONS
    if (name.isEmpty() || !name.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(this, "‚ùå Invalid name! Name must contain only letters.");
        return;
    }

    if (emailText.isEmpty() || !emailText.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
        JOptionPane.showMessageDialog(this, "‚ùå Invalid email format.");
        return;
    }

    if (pass.length() < 6) {
        JOptionPane.showMessageDialog(this, "‚ùå Password must be at least 6 characters.");
        return;
    }

    if (!phn.matches("\\d{10}")) {
        JOptionPane.showMessageDialog(this, "‚ùå Phone number must be 10 digits.");
        return;
    }

    if (addr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "‚ùå Address cannot be empty.");
        return;
    }

    // ‚úÖ ALL VALIDATIONS PASSED ‚Äî INSERT INTO DB
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/fasttrack", "root", ""
        );

        String sql = "INSERT INTO customers (full_name, phone, email, address, password) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, name);
        pst.setString(2, phn);
        pst.setString(3, emailText);
        pst.setString(4, addr);
        pst.setString(5, pass);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "‚úÖ Registered successfully!");
            // üëâ Open signin/login page
            new login().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Registration failed.");
        }

        pst.close();
        conn.close();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "üö® Error: " + e.getMessage());
    }
    }//GEN-LAST:event_submitActionPerformed

