    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String receiverAddress = reciever.getText().trim();
        String contentDetails = contents.getText().trim();

        if (receiverAddress.isEmpty() || contentDetails.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è All fields must be filled!", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int customerId = SessionManager.getCustomerId();
            Connection conn = DBConnection.getConnection();

            // üîç Get sender address from customer table
            String senderAddress = "";
            String fetchQuery = "SELECT address FROM customers WHERE customer_id = ?";
            PreparedStatement pstFetch = conn.prepareStatement(fetchQuery);
            pstFetch.setInt(1, customerId);
            ResultSet rs = pstFetch.executeQuery();

            if (rs.next()) {
                senderAddress = rs.getString("address");
            } else {
                JOptionPane.showMessageDialog(this, "‚ùå Customer not found!", "Error", JOptionPane.ERROR_MESSAGE);
                conn.close();
                return;
            }

            // üì¶ Insert into shipments table
            String insertQuery = "INSERT INTO shipments (sender, receiver, contents, status, eta) VALUES (?, ?, ?, 'Pending', NULL)";
            PreparedStatement pstInsert = conn.prepareStatement(insertQuery);
            pstInsert.setString(1, senderAddress);
            pstInsert.setString(2, receiverAddress);
            pstInsert.setString(3, contentDetails);

            pstInsert.executeUpdate();

            JOptionPane.showMessageDialog(this, "‚úÖ Delivery booked successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            reciever.setText("");
            contents.setText("");

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "‚ùå Failed to book delivery!\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

