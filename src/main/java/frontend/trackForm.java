    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    try {
        Connection con = DBConnection.getConnection();
        String sql = "SELECT DISTINCT contents FROM shipments ORDER BY contents ASC";
        PreparedStatement stmt = con.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        // üßº Clear existing items
        comboContents.removeAllItems();

        // üü¢ Add default option to show all shipments
        comboContents.addItem("All");

        // üîÅ Add unique content types
        while (rs.next()) {
            String content = rs.getString("contents");
            comboContents.addItem(content);
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "‚ùå Failed to load content types from shipments.");
    }

    }//GEN-LAST:event_jButton2ActionPerformed

