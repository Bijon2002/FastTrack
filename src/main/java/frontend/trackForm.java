    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    String selectedContent = comboContents.getSelectedItem().toString();

    try {
        Connection con = DBConnection.getConnection();

        String sql;
        PreparedStatement stmt;

        // ‚úÖ "All" shows everything
        if (selectedContent.equals("All")) {
            sql = "SELECT * FROM shipments";
            stmt = con.prepareStatement(sql);
        } else {
            sql = "SELECT * FROM shipments WHERE contents = ?";
            stmt = con.prepareStatement(sql);
            stmt.setString(1, selectedContent);
        }

        ResultSet rs = stmt.executeQuery();

        // üß± Build table model
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[] {
            "ID", "Customer ID", "Sender", "Receiver", "Contents", "Type", "Distance", "Duration"
        });

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getInt("id"),
                rs.getInt("customer_id"),
                rs.getString("sender"),
                rs.getString("receiver"),
                rs.getString("contents"),
                rs.getString("type"),
                rs.getDouble("distance"),
                rs.getString("duration")
            });
        }

        detailstb.setModel(model); // ‚úÖ Update JTable

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "‚ùå Error filtering data by contents.");
    }


    }//GEN-LAST:event_jButton1ActionPerformed

